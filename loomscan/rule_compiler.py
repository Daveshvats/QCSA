def compile_rules_for_file(file_path: Path, repo_root: Path = None,
                            llm_client=None) -> List[GeneratedRule]:
    """End-to-end: generate and verify rules for all functions in a file.

    The LLM proposes rules; mutation testing verifies them. Only verified
    rules are returned.
    """
    if not file_path.exists() or file_path.suffix != ".py":
        return []

    try:
        source = file_path.read_text(encoding="utf-8")
        tree = ast.parse(source)
    except Exception:
        return []

    rel_path = str(file_path.relative_to(repo_root)) if repo_root else str(file_path)
    language = "python"

    results: List[GeneratedRule] = []
    for node in ast.walk(tree):
        if not isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)):
            continue
        if node.name.startswith("_"):
            continue

        func_body = ast.unparse(node)
        proposed_rules = generate_rules_for_function(node.name, func_body, language, llm_client)

        for rule_dict in proposed_rules:
            yaml = rule_dict.get("yaml", "")
            if not yaml:
                continue
            # verify
            is_useful, caught = verify_rule_against_mutants(yaml, func_body, language)
            if not is_useful:
                continue  # rule doesn't catch any mutant — discard

            rule_id = f"llm-{node.name}-{hashlib.md5(yaml.encode()).hexdigest()[:8]}"
            results.append(GeneratedRule(
                rule_id=rule_id,
                function=node.name,
                file=rel_path,
                description=rule_dict.get("description", ""),
                semgrep_yaml=yaml,
                mutation_verified=True,
                mutants_caught=caught,
            ))

    return results


def save_generated_rules(rules: List[GeneratedRule], dest_dir: Path) -> List[Path]:
    """Save generated rules to .loomscan-rules/llm-compiled/."""
    dest_dir.mkdir(parents=True, exist_ok=True)
    saved: List[Path] = []
    for rule in rules:
        path = dest_dir / f"{rule.rule_id}.yml"
        path.write_text(rule.semgrep_yaml, encoding="utf-8")
        saved.append(path)
    return saved
